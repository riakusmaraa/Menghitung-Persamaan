public class MenghitungPersamaan {
    public static void main(String[] args) {
        //deklarasi
        int x, y, z;
        int a = 4;
        int b = 6;


        //proses
        x = 4*b;
        y = 2*a;
        z = (a*x)+(b*y*y);

        //output
        System.out.println("Nilai x: "+x);
        System.out.println("Nilai y: "+y);
        System.out.println("Nilai z: "+z);
    }
}
